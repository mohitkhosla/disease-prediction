                  <div class="card-body">
                    When in contact with a patient known or suspected to have active TB disease, place a surgical mask on the patient, if possible, and wear an N95 respirator (or PAPR or SCBA), if one is available. If a respirator is not available, use a surgical mask for yourself.<br><br>  

                      Not all people with active TB disease are sick enough to spread the disease and not all people who are exposed to TB become infected. Your risk of becoming infected depends on the length and intensity of the exposure and how symptomatic the sick person is. Brief or distant exposure to TB, or exposure to someone without a cough, rarely leads to infection. It usually takes lengthy contact (i.e., sharing air space for several hours over a period of several days) with someone with active TB disease for a person to become infected. Therefore, keep the length of your exposure as brief as possible.<br><br>

                      Adequate ventilation is also important. If you must transport a patient with known or suspected active TB disease, keep the windows of your vehicle open (if feasible) and set the heating and air-conditioning systems on a non-recirculating cycle.
